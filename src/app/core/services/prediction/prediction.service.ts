@Injectable({
  providedIn: 'root',
})
export class PredictionService {
  /**
   * URL base del servicio de predicciones.
   */
  baseUrl: string = 'http://127.0.0.1:8080/api/data';

  /**
   * Observable que contiene los resultados de las predicciones.
   */
  private results$ = new BehaviorSubject<any>([]);
  selectedResults$ = this.results$.asObservable();

  /**
   * Constructor del servicio de predicciones.
   * @param http Cliente HTTP para realizar solicitudes.
   */
  constructor(private http: HttpClient) {}

  /**
   * Establece los resultados de las predicciones.
   * @param results Resultados de las predicciones.
   */
  setResults(results: any) {
    this.results$.next(results);
  }

  /**
   * Realiza una predicción con los valores proporcionados.
   * @param values Valores para realizar la predicción.
   * @returns Observable con la respuesta de la solicitud.
   */
  predictValue(values: any): Observable<any> {
    return this.http.post(`${this.baseUrl}/prediction`, values, httpOptions);
  }

  /**
   * Obtiene las variables utilizadas para realizar las predicciones.
   * @returns Observable con la lista de variables.
   */
  getVariables() {
    const vars: ValuesBase<any>[] = [];
    predictionVariables.forEach(v => {
      if (v.controlType === 'dropdown') {
        vars.push(
          new DropdownQuestion({
            key: v.key,
            options: v.options,
            required: true,
          })
        );
      } else {
        vars.push(
          new Textbox({
            key: v.key,
            required: true,
          })
        );
      }
    });

    return of(vars.sort((a, b) => a.value - b.value));
  }
}
